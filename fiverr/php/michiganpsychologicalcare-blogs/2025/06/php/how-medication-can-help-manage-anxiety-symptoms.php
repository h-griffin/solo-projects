                                <h1><strong>How Medication Can Help Manage Anxiety Symptoms</strong></h1>
                                
                                <p>Taking <a href="https://www.nimh.nih.gov/health/topics/mental-health-medications">medication</a> for mental health conditions is just as valid as taking it for physical concerns. Thankfully, over the years, the stigma behind taking mental health medications has subsided. Medication can be a powerful tool in managing the symptoms of mental health concerns such as anxiety. These types of medications work most effectively when they are combined with therapy and changes in behavior.</p>
                                <h2><strong>How Medication Helps Anxiety</strong></h2>
                                <p>Anxiety can be treated with therapy, lifestyle changes, and medication. Taking <a href="https://pmc.ncbi.nlm.nih.gov/articles/PMC5573566/">psychiatric medication</a> is a great way to help reduce short and long-term symptoms associated with anxiety. By helping to reduce some of the symptoms of anxiety, people who are struggling can work through possible causes in therapy. As we mentioned above, medication can be used with other forms of treatment in order to provide the best results.&nbsp;</p>
                                <h2><strong>Common Anxiety Medications</strong></h2>
                                <ul>
                                    <li>Selective Serotonin Reuptake Inhibitors (SSRIs): SSRIs are a type of medication that helps to increase levels of serotonin in the brain. These medications are the most commonly prescribed mental health medications. SSRIs are used most commonly to treat <a href="https://pmc.ncbi.nlm.nih.gov/articles/PMC7786299/">anxiety disorders</a>, depression, and OCD.<br /><br /></li>
                                    <li>Serotonin-Norepinephrine Reuptake Inhibitors (SNRIs): SNRIs are a type of medication that increases the levels of serotonin and norepinephrine in the brain. These types of medications can be used to treat anxiety and depression. <br /><br /></li>
                                    <li>Buspirone: Buspirone is a type of medication that is used to provide relief for the short-term symptoms of anxiety. This type of medication is taken daily, unlike other medications that might be taken to relieve <a href="https://nyulangone.org/conditions/anxiety-disorders/treatments/medication-for-anxiety-disorders">symptoms of anxiety</a> in the moment. <br /><br /></li>
                                    <li>Benzodiazepines: Benzodiazepines are a type of medication that is used to treat anxiety because they slow down the central nervous system. This type of medication can be helpful to relieve anxiety or even panic attack symptoms. Benzodiazepines are taken as needed and begin acting immediately to relieve symptoms. <br /><br /></li>
                                    <li>Beta-blockers: Beta-blockers are medications that help treat symptoms of anxiety by lowering a person&rsquo;s blood pressure and heart rate. These medications, similar to Benzodiazepines, are used in the moment to relieve the symptoms of anxiety.&nbsp;</li>
                                </ul>
                                <h2><strong>What to Expect in Psychiatry Visits</strong></h2>
                                <p>Trying any new form of treatment can be scary, and that's also true for psychiatric visits. If a person hasn&rsquo;t used psychiatric medication in the past, they might be nervous about what to expect during the appointment. Below, we will discuss a few things that can help give some insight into what to expect during a psychiatric visit.&nbsp;</p>
                                <p>The first session for medication management will usually include some type of psychiatric evaluation or diagnosis. This session will be used for psychiatrists to educate their patients about medication options for them and the possible side effects. When a patient is first starting with psychiatric medication, they will likely need to schedule follow-up visits to monitor symptoms and side effects. It&rsquo;s important for patients and psychiatrists to have open communication to ensure that the medication is working the best way it can.&nbsp;</p>
                                <p>Taking medication for mental health conditions is equally valid as taking medication for a physical health condition. There is are variety of mental health medications that can be used to help treat anxiety and anxiety disorders. These medications are the most effective when used alongside talk therapy and other lifestyle changes. We understand that it can be difficult to start medication if a person has never used any before. Finding a licensed mental health professional to help you through your psychiatric journey is imperative to utilizing these life-saving medications.</p>
