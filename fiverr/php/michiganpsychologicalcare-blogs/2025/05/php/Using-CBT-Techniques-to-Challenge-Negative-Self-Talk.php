                                <h1><strong>Using CBT Techniques to Challenge Negative Self-Talk</strong></h1>
                                
                                <p>Negative thinking clouds our minds and makes all areas of our lives more difficult. These thought loops can lead to mental health conditions such as anxiety and depression. Working with a therapist can help decrease negative self-talk and challenge the validity of these thoughts.&nbsp;</p>
                                <h2><strong>What Is Negative Self-Talk?</strong></h2>
                                <p>Negative self-talk is defined as consistent thoughts that are hateful or critical towards oneself. These thoughts can become so persistent that they turn into an <a href="https://www.psychologytoday.com/us/blog/the-subplot/202407/how-to-challenge-your-inner-critic-and-win">inner critic</a>. Our Inner critic is the part of ourselves that criticizes are thoughts, words, and actions. The beliefs of the inner critic are often created in our past. These beliefs might have been told to us by another person, or they might have been how we internalized a situation.</p>
                                <h2><strong>What Is Cognitive Behavioral Therapy (CBT)?</strong></h2>
                                <p><a href="https://beckinstitute.org/about/understanding-cbt/">Cognitive Behavioral Therapy</a> (CBT) is a type of therapy that teaches people to identify the distortions in their thinking. Through CBT, patients can work with therapists to change their thoughts to be more accurate and realistic. CBT also teaches people how our thoughts, behaviors, and emotions are all connected.</p>
                                <h2><strong>What CBT Techniques Help Challenge Negative Self-Talk?</strong></h2>
                                <p>Cognitive behavioral therapy can be used to help challenge negative self-talk and reframe these beliefs. There are several different techniques that can be used to try to challenge negative thoughts. We will discuss some of these techniques below.</p>
                                <ul>
                                    <li><em>Journaling</em>: Journaling can be a great tool to use in CBT to help us identify and become aware of our negative thoughts. People can use journal prompts to help them stay focused on their thoughts they wish to change.<br /><br /></li>
                                    <li><em>Cognitive Restructuring</em>: <a href="https://pmc.ncbi.nlm.nih.gov/articles/PMC10440210/">Cognitive restructuring</a> is a part of CBT that teaches people to identify the distortions in their thinking.&nbsp; This type of reframe is typically done with the support of a therapist. An example of a distortion that could be present in negative thoughts is black and white thinking. Someone who struggles with black and white thinking typically sees things as one way or the other, with very little grey area. <br /><br /></li>
                                    <li><em>Self-Compassion Exercises</em>: After a patient can identify the distortions in their thinking and replace them with more accurate thoughts, they can work on developing <a href="https://pmc.ncbi.nlm.nih.gov/articles/PMC8019776/">self-compassion</a>. When people work through disarming their inner critic, it can be helpful to replace these thoughts with self-compassionate ones. This can be practiced through affirmations or guided meditations.</li>
                                </ul>
                                <p>Negative self-talk can make life much for difficult for the person struggling with it. Constant critical thoughts can bring hopelessness and even symptoms of depression in some cases. Thankfully, cognitive behavioral therapy offers several techniques that can be used to help challenge negative thinking. These techniques can be practiced alone or with a therapist. It may be overwhelming to try to tackle negative thoughts head-on. But we promise you that it&rsquo;s not impossible. Living a life with an inner voice supporting us is a much happier way to live.</p>
