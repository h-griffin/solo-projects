                                <h1><strong>10 Signs You're Stuck in Survival Mode</strong></h1>
                                
                                <p>Do you ever feel burnt out or that you can&rsquo;t keep going through life the way you have been? This is a common struggle for people all over the world. Oftentimes, when someone feels burnt out, they are also living in survival mode.&nbsp;</p>
                                <h2><strong>What is Survival Mode?</strong></h2>
                                <p><a href="https://www.health.harvard.edu/staying-healthy/understanding-the-stress-response">Survival mode</a> is defined as the constant activation of a person&rsquo;s autonomic nervous system. This system includes the fight, flight, and freeze responses. The fight-or-flight response is often used to describe someone&rsquo;s behavior who is <a href="https://my.clevelandclinic.org/health/diseases/11874-stress">living in survival mode</a>. This <a href="http://response">response</a> evolved as a survival mechanism over time so that people (and other groups of mammals) were able to react in situations that might threaten their lives. When a person is living in &ldquo;fight or flight&rdquo; mode, they will be experiencing hormonal and physiological changes daily that would be typically used for a person to find safety from a threat. However, when a person is in survival mode, they are oftentimes struggling with how to see their fears and stressors as they are and can overreact to things. Experiencing these changes can be hard on people&rsquo;s bodies and minds, which can simultaneously keep them stuck in survival mode. However, help is available, and recovery is possible! Below, we will go over 10 signs you are stuck in survival mode. If you reason with them, don&rsquo;t feel discouraged. Help is available, and we are here to help you get out of survival mode whenever you are ready.&nbsp;</p>
                                <ol>
                                    <li>You feel tired most of the time, regardless of how much sleep you get. When a person&rsquo;s nervous system is constantly activated, they can develop <a href="http://www.apple.com">chronic fatigue</a>.</li>
                                    <li>Your mind doesn&rsquo;t feel as sharp as usual, and you have a hard time focusing on tasks and remembering things. When a person is feeling <a href="https://www.apa.org/news/press/releases/stress/2011/impact">stressed</a> often they will likely see changes in their memory and focus. Constant stress can cause physical conditions and mental health struggles.</li>
                                    <li>You react to things quickly instead of thinking your actions through. When a person experiences fight-or-flight mode often their nervous system suffers the consequences.</li>
                                    <li>You tell people you are okay and tell yourself you don&rsquo;t need help. People living in survival mode will often try to avoid their emotions and often seek to dissociate or numb themselves from them. This response of avoiding emotions is often a trauma response and requires help in therapy.&nbsp;</li>
                                    <li>You need to change something in your life. It&rsquo;s common for people to feel stuck in their routines at work, school or in their personal lives. When a person feels stuck in these areas, they will likely experience emotional <a href="https://www.who.int/news/item/28-05-2019-burn-out-an-occupational-phenomenon-international-classification-of-diseases">burnout</a>.&nbsp;</li>
                                    <li>You force yourself to be productive even at the cost of your well-being and mental health.</li>
                                    <li>You are always worried about the next thing that is going to bring you stress or anxiety. When a person is living in survival mode, they will often experience more symptoms of anxiety and depression.&nbsp;</li>
                                    <li>You have a hard time feeling connected to others and sometimes yourself. When a person is in survival mode, they often need to learn how to <a href="https://www.psychologytoday.com/us/blog/lifting-the-veil-trauma/202006/why-survival-mode-isnt-the-best-way-live">reconnect</a> with themselves.&nbsp;</li>
                                    <li>You often ignore your basic self-care needs.</li>
                                    <li>You don&rsquo;t feel safe when you don&rsquo;t have several things going on. Sometimes, if a person has normalized chaos in their lives, they don&rsquo;t feel safe having less going on. This is a trauma response that can be treated in therapy.</li>
                                </ol>
                                <p>If you are struggling with your mental health and feel like you might be struggling with burnout or living in survival mode, please <a href="https://michiganpsychologicalcare.com/contact.php">reach out to us</a>. We are here for you and want to help you live a life outside of survival mode.</p>
