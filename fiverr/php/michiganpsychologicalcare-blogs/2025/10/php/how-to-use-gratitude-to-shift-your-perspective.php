                                <h1><strong>How to Use Gratitude to Shift Your Perspective</strong></h1>
                                <p>Sometimes in life, it can be easier to focus on the negative things happening instead of the positive things. It&rsquo;s normal for our brains to focus on the negative things in life because they are trying to protect us from potential threats. This is completely normal. However, if we can consciously shift our perspective to reflect more <a href="https://www.health.harvard.edu/healthbeat/giving-thanks-can-make-you-happier">positive thinking</a>, we will likely experience mental health and mood benefits.&nbsp;</p>
                                <h2><strong>What Is Gratitude?</strong></h2>
                                <p><a href="https://newsinhealth.nih.gov/2019/03/practicing-gratitude">Gratitude</a> is when we acknowledge and appreciate the things we are thankful for in our lives. Being grateful doesn&rsquo;t mean ignoring the negative things in life, but rather focusing on the good, regardless of what is going on in our lives. Gratitude doesn&rsquo;t always come naturally to everyone. However, choosing to focus on gratitude whenever possible will help train our brains to think more positively.</p>
                                <h2><strong>How Gratitude Shifts Your Perspective</strong></h2>
                                <p>Adopting a <a href="https://pmc.ncbi.nlm.nih.gov/articles/PMC5085264/">gratitude practice</a> helps our mindset to focus on abundance instead of what we are lacking. Having an abundant mindset helps our mental well-being and resilience to life&rsquo;s struggles. Gratitude also helps us to focus on the present moment and practice mindfulness. By staying in the present moment, we can decrease the frequency of negative thoughts. It&rsquo;s important to remember that practicing gratitude does not mean that we forget about the negative things that happen to us. Gratitude helps us to see the negative (and positive) things in a different light.</p>
                                <h2><strong>Ways to Practice Gratitude Daily</strong></h2>
                                <p>Gratitude doesn&rsquo;t have to be complicated or time-consuming. All that it takes to practice gratitude is the conscious decision to do so. However, if you are new to practicing gratitude, it may be difficult to find ways to incorporate it into your lives.</p>
                                <ul>
                                    <li>Journal: One way to practice gratitude is to have a gratitude journal. Having a gratitude journal can be done in many different ways. One way that people use gratitude journals is to list things in the morning or at night that they are grateful for. This helps us to focus on the things we are thankful for.<br /><br /></li>
                                    <li>Mindfulness: Another way to practice gratitude in our daily lives is practicing mindfulness. Mindfulness is all about staying focused on the present moment. Staying in the present moment helps to focus on what is happening right now instead of mistakes we may have made or problems we are dealing with. This can help us be grateful for the good things in our lives. <br /><br /></li>
                                    <li>Thankfulness: Practicing <a href="https://www.uclahealth.org/news/article/health-benefits-gratitude">thankfulness</a> is something that we typically do in the holiday season. <a href="https://greatergood.berkeley.edu/article/item/the_trouble_with_thanksgiving_gratitude">Thanksgiving gratitude</a> is a great way to remember the things we are thankful for in life. Even though Thanksgiving is only once a year, we can use this day to remind us how important it is to practice gratitude year-round.</li>
                                </ul>
                                <p>Life can be stressful, and staying positive can be challenging. But adopting a life filled with gratitude is an incredible way to improve mental health. We can choose to be thankful for the good things in our lives without ignoring the negative things. Being grateful is about viewing life and the things in our lives in a different way. Gratitude is a powerful tool in reminding us of all the good we already have in our lives and bringing it to our conscious attention.</p>
