                                <h1><strong>What is OCD (Obsessive Compulsive Disorder)?</strong></h1>
                                
                                <p>Obsessive-compulsive disorder is a mental health disorder that includes compulsions and obsessions that take over a person&rsquo;s life. Obsessions can be thoughts or images that persistently cause the person severe turmoil. These obsessions can be on their own or they can be paired with compulsions.&nbsp;</p>
                                <h2><strong>What is OCD?</strong></h2>
                                <p><a href="https://www.nimh.nih.gov/health/topics/obsessive-compulsive-disorder-ocd">Obsessive-compulsive</a> is a mental health condition that is marked by obsessions and compulsions. OCD often looks different for each person struggling with it. In the lens of OCD, obsessions can be defined as intrusive, persistent thoughts or images. These obsessions are so strong that they cause significant distress to the person struggling with them. Some forms of OCD only have symptoms of obsessions without having the compulsion to engage in a certain behavior. When a person is experiencing both <a href="https://iocdf.org/about-ocd/">obsessions</a> and compulsions, the compulsions will come as a result of the obsessions. Compulsions are behaviors that the person struggling with engage in to attempt to relieve the distress of the obsession. OCD can be hard for people to understand unless they are going through it themselves.&nbsp;</p>
                                <h2><strong>What Are the Symptoms of OCD?</strong></h2>
                                <p>One of the reasons why OCD is hard for people to understand is it can sometimes be seen as an invisible illness. This is because a lot of the symptoms of the disorder occur inside the person&rsquo;s head. However, some things can be common among people struggling with OCD.&nbsp;</p>
                                <p><strong>Examples of Obsessions:</strong> When someone is struggling with OCD their obsessions are usually centered around some sort of fear. Some examples of common OCD fears are fear of germs, harming others, and illness or health. Another obsession could be needing for everything to be a certain way.</p>
                                <p><strong>Examples of Compulsions:</strong> Compulsions are the behaviors that a person engages in as a result of the obsession they are dealing with. These compulsions can be repetitive behaviors that the person believes they must do. They think that if they don&rsquo;t complete their compulsions something bad will occur.</p>
                                <h2><strong>What Are Treatment Options for OCD?</strong></h2>
                                <p>The good news about obsessive-compulsive disorder is there are treatments available that can greatly help ease symptoms of OCD.&nbsp;</p>
                                <h3><strong>Therapy</strong></h3>
                                <p>Therapy can be a great tool to help someone struggling with obsessive-compulsive disorder. It can be helpful to find a therapist that specializes in or has some experience with OCD. Specifically, Cognitive Behavioral Therapy is a great tool to use when treating OCD. <a href="https://www.abct.org/get-help/what-is-cognitive-behavioral-therapy/">Cognitive Behavioral Therapy </a>(CBT) is a type of therapy that teaches how a person's emotions, thoughts and behaviors are all connected. CBT works to reframe a client&rsquo;s thoughts by recognizing distortions in them and reframing them in a more positive way. Within CBT, there is a practice called exposure and response prevention (ERP) therapy. This type of therapy helps people expose themselves to their fears in a safe environment with their therapist. Then, the therapist will support the person by preventing them from engaging in their compulsions. This can cause significant distress for the person struggling, which makes it important to do so with a licensed professional.</p>
                                <h3><strong>Medication</strong></h3>
                                <p>Medication can also be a helpful tool in treating OCD. SSRIs (selective serotonin reuptake inhibitors can be used to treat OCD. These medications work on regulating the levels of serotonin in the brain. When using medication to treat OCD, it is best paired with therapy.</p>
                                <p>Obsessive-compulsive disorder is a challenging mental health condition that can take over a person's life. However, a diagnosis of OCD does not mean hopelessness for the person struggling. There are life-saving treatments available for those struggling with OCD.</p>
