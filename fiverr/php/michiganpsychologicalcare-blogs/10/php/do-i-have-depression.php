                                <li>Low self-esteem or feelings of worthlessness, helplessness, or guilt</li>
                                <li>Cloudy thoughts and difficulties concentrating</li>
                                <li>Feeling fatigued</li>
                                <li>Physical symptoms that cannot be explained by a physical ailment</li>
