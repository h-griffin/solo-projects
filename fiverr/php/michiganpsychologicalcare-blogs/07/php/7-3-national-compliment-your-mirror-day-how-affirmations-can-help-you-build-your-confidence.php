                            <p><span style="font-weight: 400;">July 3 is National Compliment Your Mirror Day. Now, before you roll your eyes too hard at the idea of complimenting your mirror, we are here to tell you the benefits could be powerful! Affirmations are so much more than some woohoo ritual that TikTok self-help guru is sharing. Science has actually found that the practice of complimenting and talking yourself up can have many benefits to your self-esteem, interpersonal relationships, and overall enjoyment of life!</span></p>
                            <h2><strong>The Many Benefits of Affirmations</strong></h2>
                            <p><span style="font-weight: 400;">The practice of affirmations has many benefits to offer those who use them. These benefits include:</span></p>
                            <h3><strong>Reducing Stress Levels</strong></h3>
                            <p><span style="font-weight: 400;">Chronic stress levels can be detrimental to the health of your body and your brain. Fortunately, many&nbsp;</span><a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC4814782/"><span style="font-weight: 400;">researchers</span></a><span style="font-weight: 400;">&nbsp;believe that positive thinking, such as the thoughts promoted by practicing affirmations can be linked to lower stress levels and higher resilience against the emotion.</span></p>
                            <h3><strong>Motivating You</strong></h3>
                            <p><span style="font-weight: 400;">It can be easy to feel discouraged when you do not believe that you can do something. Fortunately, self-affirmations may be just what you need to believe in yourself and find the motivation to push through difficult obstacles. Getting a job done requires motivation and, with a belief in yourself and a little confidence, affirmations can help you do just that!</span></p>
                            <h3><strong>Mood Boosting</strong></h3>
                            <p><a href="https://pubmed.ncbi.nlm.nih.gov/19091166/"><span style="font-weight: 400;">Studies</span></a><span style="font-weight: 400;">&nbsp;have found that self-assuring affirmations can be critical in the healing process from mental health conditions like depression and anxiety. This is because of the mood boosting benefits offered by the practice of reframing negative thoughts into more positive ones.</span></p>
                            <h3><strong>Confidence Boosting</strong></h3>
                            <p><span style="font-weight: 400;">Your overall confidence, self-esteem, and self-worth have a huge impact on your ability to live a high-quality life. Fortunately, affirmations can be a great tool in building your confidence levels, allowing you to be happier and healthier.</span></p>
                            <h2><strong>Getting Started</strong></h2>
                            <p><span style="font-weight: 400;">As you can see, affirmations can be a great mental health tool to use when you are feeling down and lacking self-confidence. However, it isn&rsquo;t always easy to get started. A few tips to begin using affirmations in your life include:</span></p>
                            <ul>
                            <li style="font-weight: 400;"><span style="font-weight: 400;">Consider what you want to change about your thinking. Are you self-sabotaging through your negative self-thoughts? Do you struggle to stand up for yourself? Whatever it is you want to change about your thoughts and life, write down.</span></li>
                            <li style="font-weight: 400;"><span style="font-weight: 400;">Write down some positive statements about the changes you wish to make in your life. For instance, if you want to learn to say &ldquo;no&rdquo; more, a great affirmation could be &ldquo;I deserve healthy boundaries and will set them to live a life I love.&rdquo; If you want to build the confidence for that job interview, a great affirmation could be. &ldquo;I am a great candidate for this job and deserve to work here.&rdquo;</span></li>
                            </ul>
