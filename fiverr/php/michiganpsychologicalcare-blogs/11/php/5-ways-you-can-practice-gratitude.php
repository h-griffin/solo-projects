                                <h1><strong> Ways You Can Practice Gratitude for Your Mental Health - Thanksgiving (November 24)</strong></h1>

                                <p>With Thanksgiving around the corner, many of us have gratitude at the forefront of our minds. There are many reasons to express gratitude all year &lsquo;round, like making others feel appreciated and reminding yourself to appreciate the things you have.</p>
                                <h2><strong>The Many Benefits of Gratitude for Your Mental Health</strong></h2>
                                <p>Gratitude has a multitude of benefits to offer one&rsquo;s mental health. A few of these benefits include:</p>
                                <h3><strong>Feel Happier</strong></h3>
                                <p>Have you ever found that after naming the things you feel most grateful for at the Thanksgiving table, your mood is boosted, and you just feel happy? Well, science is here to back up those feelings for you! Gratitude has been associated with improved overall&nbsp;<a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC6437090/">wellbeing</a>. Many studies found gratitude participants felt an increased positive affect, better life satisfaction, higher levels of happiness, and reduced negative feelings and depression symptoms.&nbsp;&nbsp;</p>
                                <h3><strong>Decreased Depression and Anxiety Symptoms</strong></h3>
                                <p>The thinking patterns often associated with depression and anxiety are primarily negative. This is why practicing gratitude can be beneficial for reducing and lessening these symptoms, as it forces one to focus on the positive aspects of their life.</p>
                                <h3><strong>Stronger Relationships</strong></h3>
                                <p>Everyone wants to feel appreciated. Those who express gratitude toward their partners, friends, and loved ones tend to have stronger, healthier relationships.</p>
                                <h3><strong>More Resilience</strong></h3>
                                <p>Research suggests that practicing gratitude can help build one&rsquo;s resilience as they experience difficult life events. One study, in particular, focused on individuals who practiced&nbsp;<a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC9281297/">gratitude</a>&nbsp;pre-COVID, finding that they reported less severe anxiety struggles than those who did not.</p>
                                <h2><strong>5 Ways to Practice Gratitude for Your Mental Health</strong></h2>
                                <p>There are many ways you can practice gratitude. However, if you need some ideas to help you get started, we will list just a few below.</p>
                                <h3><strong>Gratitude Journal</strong></h3>
                                <p>Writing your thoughts down allows you to feel and solidify them. This can be great when you are trying to focus on gratitude. Consider the things you are grateful for and list them, doodle them, or just write about them- the possibilities are endless!</p>
                                <h3><strong>Saying Thank You</strong></h3>
                                <p>Don&rsquo;t let the little things pass without a &ldquo;thank you&rdquo;. You may find yourself taking advantage of the nights your partner does the dishes or Dad buys you lunch. However, expressing gratitude toward little acts of love and kindness can go a long way in strengthening your relationships and leaving thankfulness at the forefront of your mind.</p>
                                <h3><strong>Prayer</strong></h3>
                                <p>If you are religious, expressing your gratitude in prayer can be a truly meaningful and spiritual experience.</p>
                                <h3><strong>Gratitude Meditation</strong></h3>
                                <p>While many people focus on their breath, their body, or a phrase during meditation, you can also choose to focus on the things you are most grateful for!</p>
                                <h3><strong>Gratitude Mindfulness</strong></h3>
                                <p>As you work your way through your day, take a moment to slow down and feel grateful for every little sensation. Feel gratitude toward the sound of crunching leaves under your shoe. Feel grateful for that breeze in your hair. Feel grateful for the smell of fresh baked goods at your local bakery. There are so many little blessings we all find ourselves missing as we rush through our day-to-day. Don&rsquo;t forget to slow down and mindfully feel gratitude toward it all.</p>
                                <h2><strong>We Are Here to Help</strong></h2>
