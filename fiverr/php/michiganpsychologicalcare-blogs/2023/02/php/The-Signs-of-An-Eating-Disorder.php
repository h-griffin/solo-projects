                                <h1><strong>The Signs of An Eating Disorder- National Eating Disorders Awareness Week (February 20-26)</strong></h1>

                                <p>The week of February 20-26 has been deemed National Eating Disorders Awareness Week. Eating disorders are serious mental health conditions that can be detrimental to a person&rsquo;s physical health, self-esteem, psychological health, and overall quality of life. If left untreated, eating disorders can have potentially life-threatening implications.</p>
                                <p>Eating disorders can impact anyone, no matter their shape, size, gender, sexuality, background, or age. The more one knows about the signs of an eating disorder, the better chance one has of knowing when they or a loved one are suffering from one. This can be vital because the earlier one seeks treatment for a potential eating disorder, the better their chance of recovery. While&nbsp;<a href="https://www.nationaleatingdisorders.org/warning-signs-and-symptoms">eating disorders</a>&nbsp;can look different in different people (and in various types of eating disorders), a few of the most common signs and symptoms can be emotional, behavioral, and physical.</p>
                                <h2><strong>Emotional and Behavioral Signs and Symptoms</strong></h2>
                                <p>Some of the most common emotional signs of an eating disorder include:</p>
                                <ul>
                                    <li>An attitude or behaviors toward weight, body image, food, or diet that appears obsessive or as though they have become a significant concern. Eating disorders can lead a person to feel a need to control their body shape, diet, exercise regimen, or weight. This need can lead the person to lose control over their food intake and health. These behaviors and attitudes can include constant talking about losing weight, a need to exercise excessively after a meal, and many other concerning behaviors.</li>
                                    <li>A fixation on the foods they are consuming. As noted earlier, a person with an eating disorder is searching for control regarding their weight, diet, or body image. Because of this, they will likely become preoccupied with the foods they eat, excessively counting calories, micronutrients, macronutrients, following a strict diet, or obsessing over their body shape or weight.</li>
                                    <li>Excessive food restrictions or food rituals. A person experiencing an eating disorder may form habits around their food that allow them to feel in control, like consuming a specific amount per meal, not allowing their foods to touch, chewing food a certain amount of times, or cutting out entire food groups.</li>
                                    <li>Discomfort in eating around other people. Those experiencing eating disorders can become self-conscious about their eating habits, refusing to eat in front of other people.</li>
                                    <li>Social withdrawal. People with an eating disorder may begin withdrawing from what they once enjoyed, including participating in their favorite hobbies or hanging with their friends.</li>
                                </ul>
                                <h2><strong>Physical Signs and Symptoms</strong></h2>
                                <p>Some of the most common physical signs of an eating disorder include:</p>
                                <ul>
                                    <li>Significant changes in weight</li>
                                    <li>A weakened immune system or struggles to heal from injury</li>
                                    <li>A foggy mind</li>
                                    <li>Irregular periods</li>
                                    <li>Dizziness, lightheadedness, fainting, weakness</li>
                                    <li>Constantly feeling cold</li>
                                    <li>Hair loss, brittle hair, dental issues, discolored skin</li>
                                </ul>
