                                <h1><strong>How Pain Impacts Your Mental Health</strong></h1>

                                <p>October is National Physical Therapy Month. You may be wondering what physical therapy has to do with mental health. Physical therapy is used for healing physical pains and ailments. Did you know that these physical pains and conditions can have an impact on your mental health, as well?</p>
                                <p>Your physical and mental health are much more closely related than you may think. Thanks to this correlation, when one suffers, so does the other. Is your pain impacting your mental health?</p>
                                <h2><strong>The Relationship Between Physical and Mental Health</strong></h2>
                                <p>Our <a href="https://pubmed.ncbi.nlm.nih.gov/29132081/">mental and physical health</a> depend greatly on each other to thrive. When one is struggling, the other will struggle right along with it.&nbsp;</p>
                                <p>Part of this is because your physical and mental health depend on self-care. For the two to thrive, you must be able to:</p>
                                <ul>
                                    <li>Exercise</li>
                                    <li>Eat healthy</li>
                                    <li>Get outside</li>
                                    <li>Sleep well</li>
                                    <li>Form and maintain healthy relationships</li>
                                </ul>
                                <p>When your mental health is struggling, your energy levels and motivation to care for yourself in this way can quickly diminish. In the same way, pain and physical ailments can drain you and prevent you from performing these self-care habits.&nbsp;</p>
                                <h2><strong>What to Do When Your Pain is Impacting Your Mental Health</strong></h2>
                                <p>Pain can quickly become discouraging, impacting your mental health and overall well-being. If your pain is affecting your mental health, a few things you can do include:</p>
                                <h3><strong>Seek Professional Treatment for Your Pain</strong></h3>
                                <p>The first step to take when your pain impacts your mental health would be to treat the pain causing you distress. Reach out to your doctor to get on a professionally supported treatment plan. You may require physical therapy or further medical treatment. The quicker you can heal the pain, the better chance your mental health has at healing, as well.</p>
                                <h3><strong>Practice Mindfulness</strong></h3>
                                <p>Mindfulness can, at times, allow you to take back control of your mental health. When your thoughts fall down a rabbit hole of negativity, mindfulness may give you the power to pull yourself out. A few ways you can practice mindfulness include:</p>
                                <ul>
                                    <li>Mindful meditation</li>
                                    <li>Mindful Walking</li>
                                    <li>Breathwork</li>
                                    <li>Mindful body scan</li>
                                </ul>
                                <h3><strong>Take Care of Your Physical Health</strong></h3>
                                <p>When one part of your health suffers, it can be easy to lose the drive to care for the other parts of your well-being. However, the better care you take of your health and well-being, the better your chance of finding healing quicker.&nbsp;</p>
                                <ul>
                                    <li>Improve your sleep habits</li>
                                    <li>Eat anti-inflammatory foods</li>
                                    <li>Move your body according to your doctor&rsquo;s instructions</li>
                                </ul>
                                <h3><strong>Lean on Your Support System</strong></h3>
                                <p>A strong support system can be crucial for overcoming any hard time. Don&rsquo;t be afraid to reach out to loved ones when you need help, a listening ear, or an accountability partner. You won&rsquo;t be likely to regret reaching out.</p>
                                <h3><strong>Seek the Help of a Therapist</strong></h3>
                                <p>When your physical pain is beginning to impact your mental health, it can be incredibly helpful to talk to seek counseling. In therapy, you&rsquo;ll be able to understand why you feel the way you do, become more mindful of toxic thinking patterns, and develop healthy coping mechanisms to feel better. If you are ready to seek the help of a mental health professional, we are here for you. <a href="https://www.michiganpsychologicalcare.com/contact.php">Contact </a>us today, and we will pair you with a caring and professional therapist who will work with you on your journey toward healing.</p>
