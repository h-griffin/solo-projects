                                <h1><strong>The Impacts of Social Media on Mental Health</strong></h1>

                                <p>Social media has taken over the world as we know it. It&rsquo;s hard to go anywhere these days without finding at least one person with their head tucked into their phones. Social media offers a great opportunity to learn more about other cultures, grab your news, and stay connected with friends and family. However, there is certainly a dark side to social media.</p>
                                <h2><strong>The Impacts of Social Media on Mental Health</strong></h2>
                                <p>Social media can have many benefits and many detriments to a person&rsquo;s mental health. The impacts it has on different people will always be different, but some of the most commonly found effects of social media include:</p>
                                <h3><strong>The Pros:</strong></h3>
                                <p>Those with healthy relationships to social media use have been found to experience <a href="https://www.hsph.harvard.edu/news/features/social-media-positive-mental-health/">positive impacts on their mental health</a>, such as:</p>
                                <ul>
                                    <li>Improving feelings of emotional and social connection</li>
                                    <li>Stress relief</li>
                                    <li>Boosted self-worth</li>
                                    <li>Providing comfort, joy, or entertainment</li>
                                    <li>Preventing loneliness</li>
                                    <li>Finding new hobbies</li>
                                    <li>Growing in their education</li>
                                </ul>
                                <h3><strong>The Cons:</strong></h3>
                                <p>When a person&rsquo;s relationship with social media becomes toxic, it can begin having negative effects on their mental health, like:</p>
                                <ul>
                                    <li>Loneliness</li>
                                    <li>Depression</li>
                                    <li>Anxiety</li>
                                    <li>Comparison</li>
                                    <li>Decreased self-worth</li>
                                </ul>
                                <h2><strong>How to Keep Your Relationship with Social Media Healthy</strong></h2>
                                <h1>&nbsp;</h1>
                                <h3><strong>Set Boundaries</strong></h3>
                                <p>One of the most important things you can do to keep your relationship with social media healthy is to avoid allowing it to bleed into every aspect of your life. While there is no problem with spending time on social media, it can quickly become an issue when your entire day gets sucked away by doom-scrolling. A few boundaries you can set include:</p>
                                <ul>
                                    <li>Avoiding social media an hour before bed and an hour after waking up</li>
                                    <li>Stepping away from your phone during mealtimes</li>
                                    <li>Dedicating time every day to connecting with your loved ones without your phone distracting you</li>
                                    <li>Setting screentime limits</li>
                                </ul>
                                <h3><strong>Take Breaks as Needed</strong></h3>
                                <p>We all experience moments when we need to take a break from social media. When your relationship to social media begins to feel as though it is negatively impacting your quality of life, stepping away from social apps for a day, week, or month may be just what you need to begin feeling better.&nbsp;</p>
                                <h3><strong>Be Intentional with Your Social Media Use</strong></h3>
                                <p>Before you open that app, take a moment to consider if this is really how you wish to spend your time right now. If it is, great! You can continue what you are doing. If not, consider something you would rather be doing and do that instead! Being mindful of your social media use will ensure that the apps do what you want them to instead of what you don&rsquo;t want them to&nbsp;</p>
                                <h3><strong>Unfollow Accounts That Don&rsquo;t Bring You Joy</strong></h3>
                                <p>Social media should add joy or entertainment to your life. It should never leave you feeling down about yourself. If you find that certain accounts leave you feeling that way, there is no reason you need to see them on your newsfeed. Unfollow them and keep your social media a happy place that leaves you feeling informed, inspired, or happier than before.</p>
