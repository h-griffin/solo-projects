                                <h1><strong>Dealing with Grief in College&nbsp;</strong></h1>

                                <p>Everybody will experience loss at some point in their life or another. Whether you lose a loved one, relationship, or job, grief is a normal response to loss. When you pair this grief with the transitional and, oftentimes stressful, period a person experiences during their college years, the grief can sometimes hit harder. This College Student Grief Awareness Day, we are here to raise awareness and provide tips for getting through grief in a healthy way.</p>
                                <h2><strong>What is Grief?</strong></h2>
                                <p>Grief is a normal response to loss. Many different types of loss can trigger grief, and every single type is a valid reason. Grief impacts a person&rsquo;s emotional, mental, spiritual, and physical health and can come in many different forms, including:</p>
                                <ul>
                                    <li>Normal grief- a form of grief that tends to involve feelings of sadness, stress, shock, and loneliness that lasts less than one year</li>
                                    <li>Prolonged grief- a form of grief in which the symptoms last longer than one year</li>
                                    <li>Absent grief- a type of grief in which the person experiences grief while expressing little to no signs</li>
                                    <li>Complicated grief- a form of grief that does not seem to let up at all within six months of experiencing loss</li>
                                    <li>Anticipatory grief- a form of grief that occurs when one anticipates a death or loss</li>
                                    <li>Disenfranchised grief- grief that is complicated by the invalidation of those surrounding the person experiencing it</li>
                                    <li>Delayed grief- a form of grief in which a person&rsquo;s reaction to their loss is delayed</li>
                                </ul>
                                <h2><strong>Healthy Ways to Cope with Grief</strong></h2>
                                <p>The grief that follows a significant loss is difficult for anyone. If you have found yourself experiencing grief, finding healthy coping mechanisms to get you through it will prove crucial for your health and well-being.</p>
                                <h3><strong>Use the Resources Available to You</strong></h3>
                                <p>Most college campuses will have mental health services available to their students. Do not hesitate to take advantage of these services as you need them. You can often find useful resources on your college&rsquo;s website or by talking to the student affairs office on campus.</p>
                                <h3><strong>Allow Yourself to Feel the Way You Feel</strong></h3>
                                <p>One of the most important things you can do for yourself when experiencing grief is to honor the complicated emotions and feelings you experience as your brain works to process the loss you are experiencing. If you need to cry, allow yourself to cry. If you feel angry, find a healthy way to express that anger. Every emotion you feel throughout your grieving process is completely valid and normal.</p>
                                <h3><strong>Practice Self-Care</strong></h3>
                                <p>As tempting as it may be, try not to let your mental and physical health fall off the wagon as you work through your grief. The healthier you are, the greater your ability to get through your grief effectively. Try to continue practicing self-care by:</p>
                                <ul>
                                    <li>Keeping up with hygiene habits</li>
                                    <li>Eating enough</li>
                                    <li>Eating nutritious foods</li>
                                    <li>Getting some exercise in</li>
                                    <li>Spending time outside</li>
                                    <li>Getting enough sleep</li>
                                </ul>
                                <h3><strong>Accept Support and Help from Loved Ones</strong></h3>
                                <p>Many people begin socially isolating themselves as they experience the grieving process. Social isolation, however, is one of the most detrimental reactions a person can have when grieving. Humans need social support and interaction for resiliency and optimal health. So, surround yourself with loved ones while working through your complicated emotions. Allow them to offer you support, validation, and help as needed. Don&rsquo;t face your grief alone.</p>
                                <h2><strong>We Are Here for You</strong></h2>
                                <p>Grief is difficult for anyone of any age. Sometimes, professional help can go a long way to help you on your path to healing. Finding a grief counselor that fits your needs and lifestyle can be crucial in helping you get through this difficult time in your life. If you are ready to pursue grief therapy, our counselors are here.&nbsp;<a href="http://michiganpsychologicalcare.com/contact.php">Reach out</a>&nbsp;to us today.</p>
