                                <h1><strong>Ways to Tell if Your Teen is Struggling Mentally</strong></h1>
                                
                                <p>One's teenage years can be tumultuous and stressful. Teens face a lot of pressure to figure their lives out while dealing with significant hormonal and life changes. Because of this, some stress, rebellion, and moodiness may be expected from our teens. So how can parents or caregivers determine the difference between normal teenage behavior and mental health struggles?</p>
                                <h2><strong>The Most Common Mental Health Struggles Faced by Teens</strong></h2>
                                <p>There are many different mental health struggles a teen may encounter. According to the&nbsp;<a href="https://www.cdc.gov/childrensmentalhealth/data.html">CDC</a>, the most common of these include:</p>
                                <h3><strong>Attention Deficit Hyperactivity Disorder (ADHD)</strong></h3>
                                <p>ADHD is a prevalent mental disorder among children that leaves one struggling with a lack of focus, hyperactivity, and impulsivity. An estimated 9.8% of children aged between 3-17 years were found to have an ADHD diagnosis.</p>
                                <h3><strong>Anxiety</strong></h3>
                                <p>Anxiety disorders are another common mental disorder that causes one to experience extreme and persistent feelings of worry or fear that affect their day-to-day lives. An estimated 9.4% of children aged between 3-17 years were found to have an anxiety diagnosis.</p>
                                <h3><strong>Behavioral Problems</strong></h3>
                                <p>Behavioral problems are defined as a pattern of troublesome behaviors that cause issues in a child's schooling, home life, and social wellbeing. An estimated 8.9% of children aged between 3-17 years were found to have a behavioral problem diagnosis.</p>
                                <h3><strong>Depression</strong></h3>
                                <p>Depression is a common but serious mental illness often associated with persistent sadness, a lack of motivation, and a loss of interest in the things one used to enjoy. An estimated 4.4% of children aged between 3.17 years were found to have a depression diagnosis.</p>
                                <h2><strong>Signs Your Teen's Mental Health is Struggling</strong></h2>
                                <p>Mental health struggles can look different for everyone. Some of the most common signs that a teen may be struggling include:</p>
                                <ul>
                                    <li>Changes to sleeping patterns, weight, or eating patterns</li>
                                    <li>Loss of interest in things that once brought them joy</li>
                                    <li>Social withdrawal</li>
                                    <li>Canceling friends or family get-togethers without explanation</li>
                                    <li>A sudden increase in academic struggles</li>
                                    <li>Constant worrying</li>
                                    <li>Persistent stress or anxiety</li>
                                    <li>Refusal to open up, regardless of how safe of an environment you have created for them</li>
                                    <li>Substance use or signs of substance use</li>
                                    <li>Obsessive behaviors toward meeting a goal</li>
                                    <li>Cuts, burns, bruises, or other signs of self-harm</li>
                                    <li>Increased risky behaviors and actions</li>
                                    <li>Acting unlike themself</li>
                                    <li>Increased or intensified sexual interests</li>
                                </ul>
                                <h1>&nbsp;</h1>
                                <h2><strong>Caring for Teen Mental Health</strong></h2>
                                <p>Mental health is vital to a person's quality of life and overall wellbeing. If your teen's mental health is struggling, their physical health, happiness, social wellbeing, academic success, and career prospects will begin to struggle, too.&nbsp;</p>
                                <p>Creating a safe environment for your teen to explore their thoughts and emotions and taking the time to teach them healthy coping strategies can prove invaluable as they enter their adult years. A few ways you can help your teen care for their mental health include:</p>
                                <h3><strong>Create a Safe Environment for Sharing Thoughts, Stories, and Feelings</strong></h3>
                                <p>Social support is essential to one's overall health and wellbeing. Creating a relationship with your teen that allows them to feel comfortable sharing their wins, struggles, and emotions with you will allow them to feel supported and loved, which has been shown to provide a person with many benefits like mental resiliency and&nbsp;<a href="https://www.apa.org/topics/stress/manage-social-support#:~:text=The%20benefits%20of%20social%20support,esteem%20and%20sense%20of%20autonomy.">higher self-esteem</a>. To create a safe space for your teen to share their thoughts and feelings with you, you can:</p>
                                <ul>
                                    <li>Validate their feelings and experiences</li>
                                    <li>Build your relationship on mutual trust and respect</li>
                                    <li>Give them your full attention when they decide to open up to you</li>
                                    <li>Work on listening without judgment</li>
                                    <li>Work together to find solutions to problems</li>
                                    <li>Take the time to check in with them, and be fully open to listening when they share their experiences with you</li>
                                    <li>Respect their boundaries and privacy</li>
                                    <li>Grant them age-appropriate independence</li>
                                </ul>
                                <h3><strong>Compliment Them</strong></h3>
                                <p>It can be easy to fall into a habit of constant criticism. However, it is important to note that the way you speak to your teen will follow them for years to come. That is why it is essential to compliment them when you notice they are doing something good, too. Did you notice them handling a conflict well? Tell them! Did you see them help a friend in need? Let them know how proud you are! Don't hold back on complimenting your teen.</p>
                                <h3><strong>Work Together to Create Healthy Habits</strong></h3>
                                <p>Building healthy habits now will allow your teen a healthier start in life when they are ready to leave the nest. A few healthy habits you and your teen can work on creating include:</p>
                                <ul>
                                    <li>Taking care of their physical health by exercising, eating well, and getting enough sleep</li>
                                    <li>Relaxation techniques like meditation and breathwork</li>
                                    <li>Practicing gratitude through methods like gratitude journaling or meditation</li>
                                    <li>Limiting screentime</li>
                                    <li>Setting and upholding healthy boundaries</li>
                                    <li>Getting outside as much as possible</li>
                                </ul>
                                <h3><strong>Help Them Find Time to Rest or Have Fun</strong></h3>
                                <p>All work and no play can have devastating consequences for anyone's mental health- even your teen's. While one's teenage years may seem like the perfect opportunity to load up on extracurriculars to look good for colleges, making time for fun is critical. Encourage your teen to prioritize taking breaks and setting aside time to have fun as often as possible.</p>
                                <h3><strong>Lead by Example by Caring for Your Own Mental Health</strong></h3>
                                <p>It can be hard to help your teen care for their mental health when you are struggling with your own. Take care of yourself first by practicing healthy habits and seeking professional help when needed. As the old saying goes, "You cannot pour from an empty cup."</p>
                                <h3><strong>Getting Them Help When They Need it</strong></h3>
                                <p>If your teen seems to be struggling with their mental health, getting professional help can be crucial. The quicker you get your teen the help they need, the quicker they will be able to feel happier and healthier.</p>
                                <h2><strong>How Social Media Influences Adolescent Mental Wellness</strong></h2>
                                <p>Social media has become a significant part of all of our lives. However, when our teens spend too much time online, research has found that their mental health may begin to suffer. Social media can impact your teen's social health, leaving them struggling to connect with others in person. This lack of in-person connection can cause your teen to feel lonely and isolated, which leaves them vulnerable to mental health struggles like anxiety or depression.</p>
                                <p>Along with the social health impacts of social media comes a risk of comparison. Social media influencers and friends tend to post only the highlight reels of their life. It can become easy to get caught up in the comparison game when one is constantly consuming another's highlight reels. This can make your teen insecure in their looks, accomplishments, and life.</p>
                                <p>Another issue that can arise with increased social media use is online bullying. Many people feel more confident sharing unkind or inappropriate thoughts online than they ever would in person, as it allows them to share their most inappropriate thoughts with a sense of anonymity.&nbsp;</p>
                                <p>Setting healthy online boundaries and teaching your teen about online safety may protect them from the many dangers they face when exploring the web.</p>
