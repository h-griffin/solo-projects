                                <h1><strong>What is Dementia?</strong></h1>
                                
                                <p>March 3 is Denim Day for Dementia, intended to raise awareness about the disease and funds for those with dementia. Dementia is used to generally describe the decline in a person's cognitive functioning that is serious enough to impact their day-to-day life negatively. It is used to describe various mental ailments, like Alzheimer's disease.&nbsp;</p>
                                <h2><strong>What is Dementia?</strong></h2>
                                <p>Dementia describes social, thinking, memory, and other cognitive impairments that are severe enough to impact one's daily functioning. It is not a specific disease. Instead, it is a broad or umbrella term used to describe cognitive impairments like&nbsp;<a href="https://www.alz.org/alzheimers-dementia/what-is-dementia">Alzheimer's</a><a href="https://www.alz.org/alzheimers-dementia/what-is-dementia">disease, Huntington's disease, and frontotemporal dementia</a>.&nbsp;</p>
                                <p>Cognitive illnesses referred to as dementia are often caused by changes in the brain that trigger cognitive decline and impact one's behavior, mental health, emotional wellbeing, and relationships.</p>
                                <h2><strong>The Signs and Symptoms of Dementia&nbsp;</strong></h2>
                                <p>Dementia can have different symptoms in different cases. According to the&nbsp;<a href="https://www.alz.org/alzheimers-dementia/10_signs">Alzheimer's Association</a>, the top 10 early signs of dementia include:</p>
                                <ul>
                                    <li>Memory loss significant enough to impact daily functioning</li>
                                    <li>Problems with planning</li>
                                    <li>Problem-solving issues</li>
                                    <li>Struggling to perform familiar tasks</li>
                                    <li>Confusion in regard to the date, time, or place</li>
                                    <li>Vision problems, issues with judging distance, loss of balance</li>
                                    <li>Changes in one's ability to speak, read, or write</li>
                                    <li>Increasingly losing items and struggling to retrace steps</li>
                                    <li>Changes in judgment and decision-making</li>
                                    <li>Social withdrawal</li>
                                    <li>Significant changes to mood and personality</li>
                                </ul>
                                <h2><strong>The Causes of Dementia&nbsp;</strong></h2>
                                <p>Dementia is typically caused by changes in certain brain regions, impacting the ability of neurons and their connection's ability to function properly. There has been research that has found associations between specific cases of dementia and specific changes in the brain. However, the causes of most cases of dementia are still a mystery. Some research has suggested that certain cases of dementia may be caused by genetics, while others may be caused by lifestyle.</p>
                                <h2><strong>Caring for Cognitive Health</strong></h2>
                                <p>While the causes of many cases of dementia are still a mystery, some research has suggested that a&nbsp;<a href="https://www.alzheimers.gov/life-with-dementia/can-i-prevent-dementia">healthy lifestyle and continued learning</a>&nbsp;may improve cognitive health to help prevent diseases like dementia, although this conclusion is not yet certain. A few ways one may care for their cognitive health include:</p>
                                <ul>
                                    <li>Increased physical activity</li>
                                    <li>Control high blood pressure</li>
                                    <li>Manage blood sugar</li>
                                    <li>Eating a healthy diet</li>
                                    <li>Cognitive training and keeping the mind active</li>
                                    <li>Social connection</li>
                                    <li>Good sleep hygiene</li>
                                    <li>Caring for mental and physical health</li>
                                    <li>Reducing alcohol use</li>
                                    <li>Quitting tobacco</li>
                                </ul>
                                <p>While there is not yet enough research to say these healthy lifestyle habits can prevent dementia, they can be great ways of caring for cognitive and overall wellbeing.</p>
