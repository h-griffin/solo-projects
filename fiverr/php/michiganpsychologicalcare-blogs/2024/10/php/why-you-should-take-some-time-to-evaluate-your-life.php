                                <h1><strong>Why You Should Take Some Time to Evaluate Your Life (Evaluate Your Life Day, October 19)</strong></h1>
                                
                                <p>Life can be stressful in so many different ways. It&rsquo;s completely normal to get off track sometimes. Living life as a human means making mistakes, changing your course, and making changes in your life to improve the quality of your life. Even if you feel like it is too late for you or that you are too old to start over, these thoughts are not true. Anyone can start something new or make a change at any point in their life. All you have to do is decide the areas you want to work on.</p>
                                <p>Evaluating your life has so many benefits because it can drastically improve your overall happiness. This evaluation can lead to improvement in physical and mental health. If you take time to look at your life every few months or weeks (or whatever works for you), it&rsquo;s more likely that you will not get lost. It&rsquo;s easy to get lost in our busy world and lose touch with who we are deep down. Checking in with yourself is a great way to prevent this. Another benefit of this evaluation is avoiding burnout. Burnout can happen in many areas of our lives, but the most common ones are work and relationships.&nbsp;</p>
                                <p>The best part of evaluating your life (at any point) is that every person&rsquo;s process will be a little bit different. Some ways that you can start to assess your life are:</p>
                                <h2><strong>Goal Setting</strong></h2>
                                <p>Goal setting can be done on your own or with the help of a counselor or coach. You can set goals related to work, hobbies, health, relationships, habit forming, and anything else you put your mind to. When you are setting goals, it&rsquo;s important to set actionable steps that can be measured in some way. This will help you to have the most success with the new goals you are trying to set.</p>
                                <h2><strong>Reflective Journaling</strong></h2>
                                <p>Journaling is a great way to get to know yourself and who you are. Journaling with the intention to be reflective helps to focus your journaling on learning more about yourself. You can do this with journal prompts or guided journals. Alternatively, you can journal freely while still holding on to the intention of being self-reflective.&nbsp;</p>
                                <h2><strong>Career Counseling</strong></h2>
                                <p>Career counseling can help people find a career that they thrive in and that they find some passion in. You can choose to see a counselor that specializes in career exploration, or you can work on this with any therapist.</p>
                                <h2><strong>Therapy</strong></h2>
                                <p>Therapy is a great way to evaluate your life and your mental health. Therapists can help you objectively look at your life as an outside observer. They can help you set reasonable goals that will help you move forward. The happiness you feel in your life is directly related to the state of your mental health. Going to therapy is a great way to take care of your mental health the same way you go to the doctor&rsquo;s office for a check-up.</p>
                                <p>If you are reading this, you deserve a life filled with happiness and success. You deserve the opportunity to evaluate your life and make changes however you see fit. If you are interested in working with a counselor to evaluate your life, you can reach out to us here.</p>
