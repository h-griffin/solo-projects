                                <h1><strong>What Does Mental Illness Look Like? (Mental Illness Awareness Week, October 6)</strong></h1>
                                
                                <p>Mental illness is difficult to see from just looking at a person. Oftentimes, when people are struggling with mental illness, they will hide it from those closest to them. There are a variety of reasons why someone struggling with mental health would try to hide their issues. One of the biggest ones is the stigma that comes with mental illness in our society. This unfortunate stigma is so strong that it can even prevent people from getting help. People may feel embarrassed to ask for help because they feel like they should be able to figure it out themselves.</p>
                                <p>While it can be difficult to tell if someone is struggling with mental illness simply by looking at them, there are some signs and symptoms to look out for. Some common symptoms of mental illness are difficulty concentrating, mood changes, energy changes, weight changes, avoiding social situations, struggling with substances, and various other reasons. In more extreme cases (depending on the specific illness), the person struggling may feel like they aren&rsquo;t living in reality or they experience hallucinations. This can be dangerous for both the person struggling with mental illness and the people around them.&nbsp;</p>
                                <p>If you think that someone in your life is struggling with mental illness and you would like to approach them about it, there are a few things you can do to ensure a productive conversation. You don&rsquo;t want your loved one to seem attacked or betrayed; it&rsquo;s important to lead with you just wanting to love them and get them support if they need it. You can give them the reasons you think they may be struggling with a certain illness or how you see it affecting them. All in all, you want to make sure you listen to your loved one. Another thing to remember is not to judge them. When people are struggling, they may be having a hard time with basic tasks that most people do daily. The person struggling is likely already embarrassed about their symptoms, and adding judgment will only close them off to you and to getting help.</p>
                                <p>Over the years, there has been the development of different treatment options depending on the specific mental illness. Some of these include in-person and video counseling with a licensed therapist, medication, and group therapy. There are other types of therapy, but these are the most common ones.</p>
                                <p>As we mentioned previously, you cannot tell if someone is struggling with mental illness simply by looking at them. However, there are some symptoms you can look out for. These include changes in (the person who is struggling with mental illness) and their behavior.&nbsp;</p>
                                <p>If you have a loved one that you think is struggling with mental illness, you can approach them with love, kindness, and, most importantly, a judgment-free zone. Most of the time, if you listen to your loved one and how they are feeling, they will be more likely to be receptive to listening to your ideas involving getting help.</p>
