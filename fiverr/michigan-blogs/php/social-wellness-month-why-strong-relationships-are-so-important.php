                                <p><span style="font-weight: 400;">This month has been deemed social wellness month, making it the perfect month to dwell on just how important each and every healthy relationship in our lives is to our overall wellbeing. Every relationship we build with the people around us has a significant impact on our lives, whether that be good or bad. The healthy relationships we build can be so beneficial to our physical health, happiness, mental resilience, and overall quality of life. In this article, we are going to take a closer look at just how important having strong relationships can be for our physical, mental, and social health.</span></p>
                                <h2><strong>Strong Relationships Help You Live Longer</strong></h2>
                                <p><span style="font-weight: 400;">Yup, you read that right. Recent&nbsp;</span><a href="https://journals.plos.org/plosmedicine/article?id=10.1371/journal.pmed.1000316"><span style="font-weight: 400;">studies</span></a><span style="font-weight: 400;">&nbsp;have found that those who have a strong, healthy relationship with their life partners tend to live longer than those who do not. Now, a life partner does not always have to be a romantic connection. It can be platonic, too. One can even have multiple life partners- platonic and romantic. Both types of relationships allow one to feel as though they belong somewhere, a feeling that seems to allow many to live longer, fuller lives and build resilience against negative feelings like stress and sadness.</span></p>
                                <h2><strong>Healthy Relationships Make You Happy</strong></h2>
                                <p><span style="font-weight: 400;">As we are sure you already know, being around the people you enjoy being around can bring great joy to us. Feeling loved, accepted, and supported are all crucial feelings to living a happy, fulfilled life. Our relationships bring us support in difficult times, laughter throughout our days, and memories to look back on with true joy. And these are only a few of the many benefits strong relationships have to offer a person!</span></p>
                                <h2><strong>Relationships Help You Get Through the Difficult Moments</strong></h2>
                                <p><span style="font-weight: 400;">Healthy relationships have been shown to help individuals build resilience against difficult moments, whether that be stressful times at work, trauma, difficult thoughts and emotions, or any other reasons. Many&nbsp;</span><a href="https://www.researchgate.net/publication/233230821_Strengthening_Resilience_in_a_Risky_World_It's_All_About_Relationships"><span style="font-weight: 400;">researchers</span></a><span style="font-weight: 400;">&nbsp;believe that, rather than having individual "toughness," people who are able to get through difficult situations may actually be backed by strong, healthy relationships.</span></p>
                                <p><span style="font-weight: 400;">This idea could make perfect sense. When we feel alone and face our battles on our own, it can become easy to fall into a pit of negative self-thoughts. However, with the love and support of a friend or loved one, we have somebody to lean on when we need them the most.</span></p>
